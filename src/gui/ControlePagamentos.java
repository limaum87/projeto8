    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Parcela p = new Parcela();
        ParcelaDAO pd = new ParcelaDAO();
        DefaultTableModel modelo2 = (DefaultTableModel) jTable2.getModel();
        if (jRadioButton1.isSelected() ==false &&  jRadioButton2.isSelected() ==false && jRadioButton3.isSelected()==false  ) {
            d.MensagemErro(13);
        }else {
            if (jRadioButton1.isSelected()){
                Fechamento f = new Fechamento();
                f.setNome_fechamento(jComboBox3.getSelectedItem().toString());
                resultado=pd.ParcelaFechamento(f);
            }
            if (jRadioButton2.isSelected()){
                Fechamento f = new Fechamento();
                f.setNome_empresa(jComboBox2.getSelectedItem().toString());
                resultado=pd.ParcelaEmpresa(f);
            }
            if (jRadioButton3.isSelected()){
                Chamado f = new Chamado();
                f.setAno(jComboBox4.getSelectedItem().toString());
                f.setMes(jComboBox1.getSelectedItem().toString());
                resultado=pd.ParcelaMes(f);
            
            }
            while (modelo2.getRowCount()>0){
            modelo2.removeRow(0);
            }
            try {  
                while (resultado.next()){  
                    modelo2.addRow(new String [] {resultado.getString(1),resultado.getString(2),resultado.getString(3),resultado.getString(4),resultado.getString(5)});  
                }  
            } catch (SQLException ex) {  
                System.out.println(" Erro ao adicionar pagamento: "); 
            }
        
        }
    }//GEN-LAST:event_jButton4ActionPerformed

